			<p>Duration: 2 weeks</p>
			<h3>Role</h3>
			<p>As the lead UX Architect and Designer I was involved with all stages of the project - concept through hi-fi mocks (delivered by UI team). This included attending the kickoff meeting to understand the project at a high level, initial requirement gathering stages, research, and design phase. 
			</p>
			<p>I worked directly with key stakeholders through the requirements gathering stage and research phase. The lead AM for the presales team was my main point of contact. She  worked  with me to understand their workflow, challenges, and goals of the project. Technical questions and concerns were run through the product manager.
			</p>

		<h3>Project Goals</h3>
			<ul class="m-bottom">
				<li>To increase early client adoption and usage of Conversant Access</li>
				<li>To educate the client with onboarding launch process to allow for earlier client resource allocation and shorter launch timing</li>
				<li>To provide clients with consistent ongoing launch status and timing updates</li>
				<li>To provide clients with standardized information sharing among their team.  This will decrease miscommunication during onboarding</li>
				<li>To provide account management with single point of reference for communicating launch timing, thereby eliminating miscommunication with the client and internal teams </li>
			</ul>

